    public void btnSelectClicked(int i){
     
            try {
                int response = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa món này không?", "Xóa món",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (response == JOptionPane.NO_OPTION) {
                    fillTable();
                } else if (response == JOptionPane.YES_OPTION) {
                    phieuOrder.getUsedSelectionFood().remove(i);
                    fillTable();
                }
                
            } catch (NullPointerException e) {
            }
    }
    @Override
    public void actionPerformed(ActionEvent ae) {
        JButton btn = (JButton)ae.getSource();
        if(btn.equals(btnXacNhan)){
            btnConfirmClick();
        }
        else if(btn.equals(btnHuy)){
            btnCancelClick();
        }
        else if(btn.equals(btnTiepTuc)){
            btnTiepTucClick();
        }
    
    }
    public void fillTable() {// hien thi ket qua
        model6.setRowCount(0);
        model6.setColumnIdentifiers(new Object[]{"STT","tên", "kiểu", "giá","số lượng","thành tiền"}); 
        txtKhach.setEditable(false);
        txtBan.setEditable(false);
        txtTotalPrice.setEditable(false);
        txtKhach.setText(phieuOrder.getBooking().getClient().getName());
        txtBan.setText(phieuOrder.getBooking().getTable().getName());
        int i = 0;
        ArrayList<UsedSelectionFood> listUsedSelectionFood = new ArrayList<UsedSelectionFood>();
        listUsedSelectionFood = phieuOrder.getUsedSelectionFood();
        float totalPrice = 0;
        for (UsedSelectionFood usf : listUsedSelectionFood) {
            if(usf.getSelectionFood().getCombo()!=null){
            model6.addRow(new Object[]{i+1,usf.getSelectionFood().getCombo().getName(),usf.getSelectionFood().getCombo().getType(),usf.getSelectionFood().getCombo().getPrice(),usf.getQuantity(),usf.getQuantity()*usf.getSelectionFood().getCombo().getPrice()});
                i++;
                totalPrice +=(usf.getQuantity()*usf.getSelectionFood().getCombo().getPrice());
            }
            if(usf.getSelectionFood().getFood()!=null){
            model6.addRow(new Object[]{i+1,usf.getSelectionFood().getFood().getName(),usf.getSelectionFood().getFood().getType(),usf.getSelectionFood().getFood().getPrice(),usf.getQuantity(),usf.getQuantity()*usf.getSelectionFood().getFood().getPrice()});
                i++;
                totalPrice +=(usf.getQuantity()*usf.getSelectionFood().getFood().getPrice());
            }
        }
        txtTotalPrice.setText(String.valueOf(totalPrice));
    }
