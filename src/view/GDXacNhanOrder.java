    public void btnConfirmClick(){
        try {
            PhieuOrderDAO phieuDAO = new PhieuOrderDAO();
                Date date = new Date();
                SimpleDateFormat  formatter = new SimpleDateFormat("dd-M-yyyy hh:mm:ss");
                String thoigian = formatter.format(date);
                phieuOrder.setThoigian(thoigian);
            
                int ok = phieuDAO.savePhieuOrder(phieuOrder);
                if(ok>=0){
                    JOptionPane.showMessageDialog(rootPane,"Gọi món thành công!");
                }
                else{
                    JOptionPane.showMessageDialog(rootPane,"Gọi món thất bại!");
                }
                new GDNVOrder().setVisible(true);
                this.dispose();
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }
